// 2) create table + insert
db.serialize(() => {
  db.run(`
    CREATE TABLE IF NOT EXISTS pets (
      id INTEGER PRIMARY KEY,
      name TEXT NOT NULL,
      species TEXT NOT NULL,
      breed TEXT NOT NULL,
      age TEXT NOT NULL,
      size TEXT NOT NULL,
      gender TEXT NOT NULL,
      color TEXT NOT NULL,
      energy TEXT NOT NULL,
      goodWithKids INTEGER NOT NULL,
      goodWithPets INTEGER NOT NULL,
      description TEXT NOT NULL,
      traits TEXT NOT NULL,
      location TEXT NOT NULL,
      shelter TEXT NOT NULL,
      shelterId INTEGER NOT NULL,
      image TEXT NOT NULL,
      images TEXT NOT NULL
    );
  `);

  const stmt = db.prepare(`
    INSERT OR REPLACE INTO pets (
      id, name, species, breed, age, size, gender, color, energy,
      goodWithKids, goodWithPets, description, traits, location,
      shelter, shelterId, image, images
    ) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)
  `);

  pets.forEach((pet) => {
    stmt.run(
      pet.id,
      pet.name,
      pet.species,
      pet.breed,
      pet.age,
      pet.size,
      pet.gender,
      pet.color,
      pet.energy,
      pet.goodWithKids ? 1 : 0,
      pet.goodWithPets ? 1 : 0,
      pet.description,
      JSON.stringify(pet.traits),
      pet.location,
      pet.shelter,
      pet.shelterId,
      pet.image,
      JSON.stringify(pet.images)
    );
  });

  stmt.finalize(() => {
    console.log(`Inserted/updated ${pets.length} pets.`);
    db.close();
  });
});
